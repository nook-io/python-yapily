import json
import pprint

from pydantic import BaseModel, ConfigDict, StrictStr


class TransactionHash(BaseModel):
    """
    An unique identifier for the transaction that was generated by Yapily. This is a hash of transaction attributes and may be useful an id is not present (and therefore hasn't been provided by an Institution).  # noqa: E501
    """

    hash: StrictStr | None = None
    __properties = ["hash"]
    model_config = ConfigDict(populate_by_name=True, validate_assignment=True)

    def to_str(self) -> str:
        """Returns the string representation of the model using alias"""
        return pprint.pformat(self.model_dump(by_alias=True))

    def to_json(self) -> str:
        """Returns the JSON representation of the model using alias"""
        return json.dumps(self.to_dict())

    @classmethod
    def from_json(cls, json_str: str) -> "TransactionHash":
        """Create an instance of TransactionHash from a JSON string"""
        return cls.from_dict(json.loads(json_str))

    def to_dict(self):
        """Returns the dictionary representation of the model using alias"""
        return self.model_dump(by_alias=True, exclude_none=True)

    @classmethod
    def from_dict(cls, obj: dict) -> "TransactionHash":
        """Create an instance of TransactionHash from a dict"""
        if obj is None:
            return None

        if not isinstance(obj, dict):
            return TransactionHash.model_validate(obj)

        return TransactionHash.model_validate({"hash": obj.get("hash")})
